def soma(n1, n2):
    soma = n1 + n2
    return soma

def subtracao(n1, n2):
    subtracao = n1 - n2
    return subtracao

def multiplicacao (n1, n2):
    multiplicacao = n1 * n2
    return multiplicacao

def divisao (n1, n2):
    divisao = n1 / n2
    return divisao

